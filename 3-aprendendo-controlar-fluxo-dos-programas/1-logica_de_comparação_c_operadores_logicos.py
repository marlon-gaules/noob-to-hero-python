# comprações numéricas
idade = 15
print(idade > 17)
print(idade < 17)
print(idade <= 17)
print(idade >= 17)
print(idade == 17)
# totalmente diferente
print(idade != 17)

# comparações entre outros tipos
print(True == True)
print("Rafael" == "Rafael")
print("Rafael" == "rafael")
print("f" > "c")  # comparação na posição do alfabeto
print(5 == "5")
